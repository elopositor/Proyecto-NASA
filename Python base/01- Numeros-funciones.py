# Funciones nativas de python (solo tiene básicas)
import math
print(round(1.2))
print(abs(-77))

# Modulo math: incluye funciones para manejo de datos

print(math.ceil(1.1))  # Redondea al integer más alto
print(math.floor(1.99))  # Redondea al integer más bajo
print(math.isnan(1))  # Devuelve boolean indicando si es NA o no
print(math.pow(10, 3))  # Eleva un número a la potencia
print(math.sqrt(9))  # Raiz cuadrada

